class IMP_Ajax_Application_Handler_SaveId extends Horde_Core_Ajax_Application_Handler
{
    /**
     * AJAX action: SaveId.
     */
    public function saveId()
    {
        global $injector, $session, $page_output, $vars;

        $id = $this->vars->param;

        try {
            $session->set('imp', 'identity', $id);
        } catch (\Throwable $th) {
            throw $th;
        }

        return true;
        //\Horde::debug($session->get('imp', 'identity'), '/dev/shm/backend', false);
    }

    /**
     * Ajax action: get the smime base url and pass it to js
     */
    public function getSmimeUrl(){
        $smime_url = IMP_Basic_Smime::url();
        return $smime_url->url;  
    }

    /**
     * Ajax action: return the reload Url as generated by UI class of Smime
     */ 
    public function getReload(){
        $url = base64_encode($ui->selfUrl()->setRaw(true));
    }


}
